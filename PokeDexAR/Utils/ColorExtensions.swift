import Foundation
import UIKit

extension UIColor {
    
    static var buttonNormalColor: UIColor {
        return UIColor(red: 80/255, green: 84/255, blue: 196/255, alpha: 1)
    }
    
    static var buttonHighlightColor: UIColor {
        return UIColor(red: 76/255, green: 79/255, blue: 174/255, alpha: 1)
    }
    
    static var lightBlackColor: UIColor {
        return UIColor(red: 44/255, green: 44/255, blue: 44/255, alpha: 1)
    }
}
